package day2.variables;

public class swapingnumbers {

	public static void main(String[] args) {
		int number1=5;
		int number2=10;
		int temp;
		System.out.println("before swap number 1 is"+number1);
		System.out.println("before swap number 2 is"+number2);
		temp=number1;
		number1=number2;
		number2=temp;
		System.out.println("number 1 is"+number1);
		System.out.println("number 2 is"+number2);
		
	}

}

